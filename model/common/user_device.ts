/**
 * 设备
 * @export
 * @class UserDevice
 */
export class UserDevice {
    /**
     * 设备类型：DESKTOP PHONE TABLET CAR BOT
     *
     * @type {string}
     * @memberOf UserDevice
     */
    public DeviceType: string;

    /**
     * 设备名称
     *
     * @type {string}
     * @memberOf UserDevice
     */
    public DeviceName: string;

    /**
     * 操作系统
     *
     * @type {string}
     * @memberOf UserDevice
     */
    public OS: string;

    /**
     * 操作系统版本
     *
     * @type {string}
     * @memberOf UserDevice
     */
    public OSVersion: string;

    /**
     * 浏览器内核
     *
     * @type {string}
     * @memberOf UserDevice
     */
    public Engine: string;

    /**
     * 浏览器内核版本
     *
     * @type {string}
     * @memberOf UserDevice
     */
    public EngineVersion: string;

    /**
     * 浏览器类型
     *
     * @type {string}
     * @memberOf UserDevice
     */
    public Browser: string;

    /**
     * 浏览器版本
     *
     * @type {string}
     * @memberOf UserDevice
     */
    public BrowserVersion: string;

    /**
     * 浏览器主版本号
     *
     * @type {string}
     * @memberOf UserDevice
     */
    public BrowserMajor: string;

    /**
     * CPU
     *
     * @type {string}
     * @memberOf UserDevice
     */
    public CPU: string;
}